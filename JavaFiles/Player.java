public class Player {
    String name;
    int age;
    String team;
    String league;
    int gamesPlayed;

    public String getName() {
        return name;
    }
    public int getAge() {
        return age;
    }

    public String getTeam() {
        return team;
    }

    public String getLeague() {
        return league;
    }

    public int getGamesPlayed() {
        return gamesPlayed;
    }
}
